package businesscomponents;

public class CustRelatedAccount {
	
	private String acctId;
	private String acctName;
	private String acctType;
	private String acctSubType;
	private String acctRole;
	
	
	public String getAcctId() {
		return acctId;
	}
	public void setAcctId(String acctId) {
		this.acctId = acctId;
	}
	public String getAcctName() {
		return acctName;
	}
	public void setAcctName(String acctName) {
		this.acctName = acctName;
	}
	public String getAcctType() {
		return acctType;
	}
	public void setAcctType(String acctType) {
		this.acctType = acctType;
	}
	public String getAcctSubType() {
		return acctSubType;
	}
	public void setAcctSubType(String acctSubType) {
		this.acctSubType = acctSubType;
	}
	public String getAcctRole() {
		return acctRole;
	}
	public void setAcctRole(String acctRole) {
		this.acctRole = acctRole;
	}
	
	

}
